uint8_t crc8_check(uint8_t *data , uint16_t len)
{
	uint16_t i   = 0;
	uint8_t  fcs = 0;
	for(i=0 ; i<(len) ; i++)
	{
		fcs = crc8_table[(uint8_t)fcs ^ (*data)];
		data++;
	}
	return fcs;
}
